package com.example.demo.model;

public class HeatmapData {
    private String branch;
    private int general;
    private int obc;
    private int sc;
    private int st;
    private int ews;

    // Getters and Setters
    public String getBranch() {
        return branch;
    }

    public void setBranch(String branch) {
        this.branch = branch;
    }

    public int getGeneral() {
        return general;
    }

    public void setGeneral(int general) {
        this.general = general;
    }

    public int getObc() {
        return obc;
    }

    public void setObc(int obc) {
        this.obc = obc;
    }

    public int getSc() {
        return sc;
    }

    public void setSc(int sc) {
        this.sc = sc;
    }

    public int getSt() {
        return st;
    }

    public void setSt(int st) {
        this.st = st;
    }

    public int getEws() {
        return ews;
    }

    public void setEws(int ews) {
        this.ews = ews;
    }
}
